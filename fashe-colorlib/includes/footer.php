		$('.message').click(function(){
			var name   = $("#name").val();
			var phone  = $("#phone").val();
			var email  = $("#email").val();
			var message= $("#message").val();
			if (!name) {
				$("#name").attr("placeholder","Please enter your name");
			}
			if (!phone) {
				$("#phone").attr("placeholder","Please enter your phone");
			}
			if(!email){
				$("#email").attr("placeholder","Please enter your email");
			}
			if (!message) {
				$("#message").attr("placeholder","Please enter your message");
			}
			if (name && phone && email && message) {
				$.ajax({
					type :"POST",
					url  :"fun/message.php",
					data :{
						'name'  : name,
						'phone' : phone,
						'email' : email,
						'message':message,
					},
					success:function(data){
						$("#name").val('');
						$("#phone").val('');
						$("#email").val('');
						$("#message").val('');
						$(".data-form").html('<span styly="color : red;">Your message has sent successfully</span>' + data)
					}
				})
			}
		})
	</script>
